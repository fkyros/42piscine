int	ft_str_is_numeric(char *str)
{
	int	i;
	int	b;

	b = 1;
	i = 0;
	if (*str == '\0')
		return (1);
	while (b == 1 && str[i] != '\0')
	{
		if (!(str[i] >= '0' && str[i] <= '9'))
			b = 0;
		i++;
	}
	return (b);
}
