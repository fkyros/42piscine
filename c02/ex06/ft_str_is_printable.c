int	ft_str_is_printable(char *str)
{
	int	n;
	int	b;

	n = 0;
	b = 1;
	if (*str == '\0')
		return (1);
	while (b == 1 && str[n] != '\0')
	{
		if (!(str[n] >= 32 && str[n] <= 127))
			b = 0;
		n++;
	}
	return (b);
}
