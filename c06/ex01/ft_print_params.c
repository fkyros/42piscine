#include <unistd.h>

int	main(int argc, char **argv)
{
	int	i;
	int	n;

	n = 1;
	i = 0;
	while (argc > n)
	{
		while (argv[n][i] != '\0')
		{
			write(1, &argv[n][i], 1);
			i++;
		}
		write(1, "\n", 1);
		i = 0;
		n++;
	}
	return (0);
}
